You can use the short echo tag to <?= 'print this string' ?>.<br>
It's equivalent to <?php echo 'print this string'; ?>.<br>
     
<?php
    echo 'This is a test'; // This is a one-line c++ style comment
    /* This is a multi line comment
       yet another line of comment */
    echo 'This is yet another test';
    echo 'One Final Test'; # This is a one-line shell-style comment
?>  

<h1>This is an <?php # echo 'simple';?> example</h1> <!--comment-->
<p>The header above will say 'This is an  example'.</p> 

<?php
    echo "Hello world";
    echo "Last statement"; // the script ends here with no PHP closing tag

